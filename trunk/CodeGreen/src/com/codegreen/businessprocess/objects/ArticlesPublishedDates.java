package com.codegreen.businessprocess.objects;

public class ArticlesPublishedDates {

	private String imagePublishedDate;
	private String textPublishedDate;
	private String audioPublishedDate;
	private String videoPublishedDate;
	public String getImagePublishedDate() {
		return imagePublishedDate;
	}
	public void setImagePublishedDate(String imagePublishedDate) {
		this.imagePublishedDate = imagePublishedDate;
	}
	public String getTextPublishedDate() {
		return textPublishedDate;
	}
	public void setTextPublishedDate(String textPublishedDate) {
		this.textPublishedDate = textPublishedDate;
	}
	public String getAudioPublishedDate() {
		return audioPublishedDate;
	}
	public void setAudioPublishedDate(String audioPublishedDate) {
		this.audioPublishedDate = audioPublishedDate;
	}
	public String getVideoPublishedDate() {
		return videoPublishedDate;
	}
	public void setVideoPublishedDate(String videoPublishedDate) {
		this.videoPublishedDate = videoPublishedDate;
	}
	
}
